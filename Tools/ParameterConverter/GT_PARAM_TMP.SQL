CREATE TABLE GT_PARAM_TMP
(
  PNAME VARCHAR2 (40 CHAR) NOT NULL,
  UNITGROUPNO NUMBER (5,0) NOT NULL,
  PRACNO NUMBER (2,0) NOT NULL,
  TYPE NUMBER (1,0) NOT NULL,
  UNIT VARCHAR2 (10 CHAR),
  VALUE VARCHAR2 (30 CHAR),
  INST_ACTV NUMBER (1,0) NOT NULL,
  DIALOG_PARAM NUMBER (1,0) NOT NULL,
  CONV NUMBER (20,7) DEFAULT 1. NOT NULL,
  MIN_VALUE NUMBER (20,7) DEFAULT 0. NOT NULL,
  MAX_VALUE NUMBER (20,7) DEFAULT 0. NOT NULL,
  CREATIONDATE DATE NOT NULL,
  PARAMDESC VARCHAR2 (100 CHAR) DEFAULT 'NOT SET',
  PARAMDESC_C VARCHAR2 (100 CHAR) DEFAULT 'NOT SET',
  PARAMGROUP VARCHAR2 (10 CHAR)
)
PCTFREE 10
INITRANS 1
MAXTRANS 255
TABLESPACE USERS
STORAGE
(
  INITIAL 128 K
  NEXT 128 K
  MINEXTENTS 1
  MAXEXTENTS UNLIMITED
  PCTINCREASE 0
  BUFFER_POOL DEFAULT
)
NOCACHE
NOPARALLEL
NOLOGGING
/

