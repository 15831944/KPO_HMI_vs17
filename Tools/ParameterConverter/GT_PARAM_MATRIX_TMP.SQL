'Parameter name'
/

COMMENT ON COLUMN GT_PARAM_MATRIX_TMP.TYPE IS
'Type indicator'
/

COMMENT ON COLUMN GT_PARAM_MATRIX_TMP.VALUE IS
'Value of entry'
/

