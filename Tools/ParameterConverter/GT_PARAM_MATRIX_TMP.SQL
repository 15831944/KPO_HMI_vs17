CREATE TABLE GT_PARAM_MATRIX_TMP
(
  PNAME VARCHAR2 (40 CHAR) NOT NULL,
  UNITGROUPNO NUMBER (5,0) NOT NULL,
  PRACNO NUMBER (2,0) NOT NULL,
  TYPE NUMBER (1,0) NOT NULL,
  KEY1 VARCHAR2 (30 CHAR) NOT NULL,
  KEY2 VARCHAR2 (30 CHAR) NOT NULL,
  VALUE VARCHAR2 (30 CHAR)
)
PCTFREE 10
INITRANS 1
MAXTRANS 255
TABLESPACE USERS
STORAGE
(
  INITIAL 64 K
  NEXT 64 K
  MINEXTENTS 1
  MAXEXTENTS UNLIMITED
  PCTINCREASE 0
  BUFFER_POOL DEFAULT
)
NOCACHE
NOPARALLEL
NOLOGGING
/

